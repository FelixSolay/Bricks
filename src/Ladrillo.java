
public class Ladrillo {
	private int largo;
	public boolean sacar = false;
	
	public Ladrillo(int largo) {
		this.largo = largo;
	}

	public int getLargo() {
		return largo;
	}

	public boolean isSacar() {
		return sacar;
	}

	public void setLargo(int largo) {
		this.largo = largo;
	}

	public void setSacar(boolean sacar) {
		this.sacar = sacar;
	}
	
	
}
